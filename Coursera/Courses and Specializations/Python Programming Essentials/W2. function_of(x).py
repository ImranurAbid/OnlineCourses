def function_of(x):
    equation = (-5 * x ** 5) + (67 * x ** 2) - 47
    return equation
    
print("f(0) =", function_of(0))
print("f(1) =", function_of(1))
print("f(2) =", function_of(2))
print("f(3) =", function_of(3))

